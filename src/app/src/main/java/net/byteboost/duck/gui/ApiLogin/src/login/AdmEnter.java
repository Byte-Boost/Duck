    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Usuario Usuario = new Usuario();
            Usuario.setUsuario(jTextField1.getText());

       // fazendo a validação dos dados
       if ((jTextField1.getText().isEmpty())) {
              JOptionPane.showMessageDialog(null, "O campo não podem retornar vazio");
        }
        else {

          // instanciando a classe ClienteDAO do pacote dao e criando seu objeto dao
          UsuarioDAO dao = new UsuarioDAO();
          dao.adiciona(Usuario);
          JOptionPane.showMessageDialog(null, "Usuario "+jTextField1.getText()+" inserido com sucesso! ");
         }

       // apaga os dados preenchidos nos campos de texto
       jTextField1.setText("");
    }                                   

        
